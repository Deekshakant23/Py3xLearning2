# Arithmetic Operators
# +,-,*,/, %
a = 100
b = 50
print(a + b)  #output 150
print(a - b)  #output 50
print(a * b)  #output 5000
print(a / b)  #output 2.0   # Float - Why Python is smart - div

print(a % b)   #modulus will be the output
print(10 ** 2)
r = pow(10, 2)
print(r)


